    private void openSprintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openSprintActionPerformed
        Sprint result = (Sprint) JOptionPane.showInputDialog(null,
                "Choose a Sprint",
                "Open Sprint",
                JOptionPane.QUESTION_MESSAGE,
                null,
                sprints.toArray(),
                sprints.get(0));
        if (result != null){
            new SprintFrame(result).setVisible(true);
        }
    }//GEN-LAST:event_openSprintActionPerformed

    private void addToSprintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToSprintActionPerformed
        if (list.getSelectedIndex() != -1) {
            Sprint result = (Sprint) JOptionPane.showInputDialog(null,
                    "Choose a Sprint",
                    "Add Story to Sprint",
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    sprints.toArray(),
                    sprints.get(0));
            if (result != null) {
                result.insertStory((UserStory) list.getSelectedValue());
            }
            
        }
    }//GEN-LAST:event_addToSprintActionPerformed

