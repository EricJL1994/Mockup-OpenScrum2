    private static DefaultListModel model;
    private static DefaultListModel model2;
    private static ArrayList<Sprint> sprints = new ArrayList<>();

    public static void refreshList() {
        model = new DefaultListModel();
        ArrayList<UserStory> backlog = pb.getBacklog();
        for (UserStory story : backlog) {
            model.addElement(story);
        }
        list.setModel(model);
    }

    public static void refreshList2() {
        UserStory story = (UserStory) list.getSelectedValue();
        if (story != null) {
            model2 = new DefaultListModel();
            ArrayList<Resource> resource = story.getResources();
            for (Resource r : resource) {
                model2.addElement(r.GetName());
            }
            list_resources.setModel(model2);
        }
    }

    public static void removeResource() {
        int story_index = list_resources.getSelectedIndex();
        int resource_index = list.getSelectedIndex();
        if ((story_index != -1) && (resource_index != -1)) {
            pb.getBacklog().get(resource_index).getResources().remove(story_index);
        }
    }
